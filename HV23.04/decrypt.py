def decrypt_flag(encrypted_flag):
    decrypted_flag = []
    
    for char in encrypted_flag:
        decrypted_char = ~ord(char) & 0xFF
        decrypted_flag.append(chr(decrypted_char))

    return ''.join(decrypted_flag)

